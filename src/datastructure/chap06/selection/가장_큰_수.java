package datastructure.chap06.selection;

public class 가장_큰_수 {

    class Solution {
        public String solution(int[] numbers) {
            String answer = "";

            String[] str = new String[numbers.length];

            for (int i = 0; i < str.length; i++) {
                str[i] = String.valueOf(numbers[i]);
            }

            return answer;
        }
    }

}
